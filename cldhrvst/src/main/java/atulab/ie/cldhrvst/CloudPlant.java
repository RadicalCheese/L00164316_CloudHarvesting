package atulab.ie.cldhrvst;

/**CloudPlant class
 * creates CloudPlant objects
 * @author Isabel Conaghan
 */

public class CloudPlant {
	
	private int numClouds;
	private String name;
	private String colour;
	private String purpose;
	
	
	/**
	 * @return the numClouds
	 */
	public int getNumClouds() {
		return numClouds;
	}
	/**
	 * @param numClouds the numClouds to set
	 */
	public void setNumClouds(int numClouds) {
		this.numClouds = numClouds;
	}
	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}
	/**
	 * @param name the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}
	/**
	 * @return the colour
	 */
	public String getColour() {
		return colour;
	}
	/**
	 * @param colour the colour to set
	 */
	public void setColour(String colour) {
		this.colour = colour;
	}
	/**
	 * @return the purpose
	 */
	public String getPurpose() {
		return purpose;
	}
	/**
	 * @param purpose the purpose to set
	 */
	public void setPurpose(String purpose) {
		this.purpose = purpose;
	}
	
	
	

}
